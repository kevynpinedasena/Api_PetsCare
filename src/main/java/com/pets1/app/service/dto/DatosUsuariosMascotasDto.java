package com.pets1.app.service.dto;

public class DatosUsuariosMascotasDto {

	private String nombreUs;
	private String correoUs;
	private String telefonoUs;
	private String nombreMasc;
	private String razaMasc;
	private String tipoMasc;
	public String getNombreUs() {
		return nombreUs;
	}
	public void setNombreUs(String nombreUs) {
		this.nombreUs = nombreUs;
	}
	public String getCorreoUs() {
		return correoUs;
	}
	public void setCorreoUs(String correoUs) {
		this.correoUs = correoUs;
	}
	public String getTelefonoUs() {
		return telefonoUs;
	}
	public void setTelefonoUs(String telefonoUs) {
		this.telefonoUs = telefonoUs;
	}
	public String getNombreMasc() {
		return nombreMasc;
	}
	public void setNombreMasc(String nombreMasc) {
		this.nombreMasc = nombreMasc;
	}
	public String getRazaMasc() {
		return razaMasc;
	}
	public void setRazaMasc(String razaMasc) {
		this.razaMasc = razaMasc;
	}
	public String getTipoMasc() {
		return tipoMasc;
	}
	public void setTipoMasc(String tipoMasc) {
		this.tipoMasc = tipoMasc;
	}
	
	
}
